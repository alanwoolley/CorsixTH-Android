package uk.co.armedpineapple.cth.stats

/**
 * Gameplay statistics
 */
enum class Statistic {
    /**
     * The number of cured patients.
     */
    CURES,

    /**
     * The number of killed patients.
     */
    KILLS,

    /**
     * The total amount of money earned.
     */
    MONEY_EARNED,

    /**
     * The total amount of money lost.
     */
    MONEY_LOST,

    /**
     * The total sum of loans taken.
     */
    LOAN_TAKEN
}
